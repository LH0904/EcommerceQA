"""
预设问题模板 - 按类别组织
"""

PRESET_QUESTIONS = {
    "用户分析": [
        "男女用户比例是多少",
        "各城市的用户数量分布",
        "各职业用户的平均消费金额",
        "用户活跃度排行前10",
        "各年龄段用户数量统计",
    ],
    "商品分析": [
        "最受欢迎的前10个商品类别",
        "各行为类型的数量分布",
        "销量最高的前10个商品",
        "收藏量最高的商品",
        "各价格区间的商品数量",
    ],
    "销售趋势": [
        "每天的购买量趋势",
        "每周的浏览量变化",
        "各城市的购买转化率",
        "购买金额最高的前10个城市",
        "各行为类型的转化率分析",
    ],
    "综合分析": [
        "用户购买行为漏斗分析",
        "男女用户购买偏好对比",
        "各城市消费能力排行",
        "商品热度与销量关系分析",
        "用户类型分布统计",
    ],
}


def get_category(question: str) -> str:
    """判断问题是否属于预设类别，返回类别名或 '自定义'"""
    for cat, questions in PRESET_QUESTIONS.items():
        if question in questions:
            return cat
    return "自定义"
